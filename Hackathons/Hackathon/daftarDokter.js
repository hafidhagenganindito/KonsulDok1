// DOM EKSTERNAL utk menghubungkan dan mengelola file html di JS
const tanggal = document.querySelector("form");

tanggal.addEventListener("submit", (e) => {
  e.preventDefault();
  document.querySelector(".row").innerHTML = "";
  const data = new FormData(tanggal);
  const objData = Object.fromEntries(data);
  localStorage.setItem("jadwal", objData.birthday);
  let tanggalHari = objData.birthday[8] + objData.birthday[9];
  if (objData.birthday[8] === "0") {
    tanggalHari = objData.birthday[9];
  }
  localStorage.setItem("tanggal", tanggalHari);
  const spesialisasi = localStorage.getItem("spesialis");
  console.log(spesialisasi);

  for (let dokters of dokter[spesialisasi]) {
    for (let tanggalTugas of dokters.tanggal) {
      console.log(tanggalHari, "===", String(tanggalTugas));
      if (tanggalHari === String(tanggalTugas)) {
        let insert = `
                <div class="col-lg-4 col-md-6 d-flex align-items-stretch">
                <div class="member">
                    <a href="Form.html" onclick="localStorage.setItem('id', ${dokters.id})"><img src="${dokters.img}" alt="">
                    <h4>${dokters.nama}</h4></a>
                    <span >${dokters.spesialis}</span>
                </div>
                </div>
                `;
        document.querySelector(".row").innerHTML += insert;
      }
    }
  }
});

